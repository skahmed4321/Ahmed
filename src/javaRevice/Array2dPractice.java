package javaRevice;

public class Array2dPractice {
    public static void main(String[] args) {
        int[][] c= {{7,8,9},{10,11,12}};
        for (int i = 0; i < 2; i++){
            for (int k = 0; k < 3; k++){
                System.out.print(c[i][k]+" ");
            }
            System.out.println();
        }

    }
}
