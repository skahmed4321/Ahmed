package javaRevice;

public class Switch2 {

    public static void main(String[] args) {

char findLetter = 'u';
switch(findLetter){

    case 'a':
        System.out.println("its vowel");
        break;

    case 'e':
        System.out.println("its vowel1");
        break;
    case 'i':
        System.out.println("its vowel2");
        break;
    case 'o':
        System.out.println("its vowel3");
        break;
    case 'u':
        System.out.println("its vowel4");
        break;
    default:
        System.out.println("not found");

}

    }
}
